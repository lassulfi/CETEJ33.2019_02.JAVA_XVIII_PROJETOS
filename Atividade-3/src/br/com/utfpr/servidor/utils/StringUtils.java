package br.com.utfpr.servidor.utils;

/**
 * Classe utilitária para operações com Strings
 * 
 * @author Luis Daniel Assulfi
 *
 */
public class StringUtils {

	/**
	 * Retorna uma String convertida para caixa alta (uppercase)
	 * @param message
	 * @return {@link String} convertida para caixa alta
	 */
	public static String convertToUpperCase(String message) {
		return message.toUpperCase();
	}
}
